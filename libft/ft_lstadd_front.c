#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	*alst = ft_lstfirst(*alst);
	(*alst)->previous = new;
	new->next = *alst;
	*alst = new;
}
